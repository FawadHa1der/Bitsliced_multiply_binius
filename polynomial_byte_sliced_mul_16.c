int compare_m512i_arrays(__m512i* a, __m512i* b, int size) {
  for (int i = 0; i < size; i++) {
    __m512i cmp = wrapper_mm512_xor_si512(a[i], b[i]);
    if (_mm512_test_epi64_mask(cmp, cmp)) {

      return 0; // if they are not equal
    }
  }
  return 1; // if they are equal
}


